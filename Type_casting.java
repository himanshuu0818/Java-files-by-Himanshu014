public class Type_casting {
    public static void main(String[] args) {
      
      
      // widening type casting   ------>  automatically
        byte x = 10;
        int y = x;
        System.out.println(y);

         // Narrowing type casting -----> manual
         long a = 123;
         byte b = (byte) a;
         System.out.println(b);

    }
}
